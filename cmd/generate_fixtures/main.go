func init() {
	flag.StringVar(&flagOutputDir, "output", "", "set output directory path")
}

func buildImage(alpha uint8) (*image.RGBA, *image.Gray) {
	colors := make([]uint8, 30*42)
	copy(colors, rawFx6)

	imgRGBA := image.NewRGBA(image.Rect(0, 0, 30, 42))
	imgGray := image.NewGray(image.Rect(0, 0, 30, 42))

	for y := 0; y < 42; y++ {
		for x := 0; x < 30; x++ {
			var er, eg, eb uint8
			e := colors[(30*y)+x]

			if e == 1 {
				er = 0xff
				eg = 0xff
				eb = 0xff
			} else {
				er = 0x00
				eg = 0x00
				eb = 0x00
			}

			c := color.RGBA{R: er, G: eg, B: eb, A: alpha}
			imgRGBA.SetRGBA(x, y, c)
			imgGray.Set(x, y, color.GrayModel.Convert(c))
		}
	}

	return imgRGBA, imgGray
}

func checkErr(err error) {
	if err != nil {
		panic(err)
	}
}

func saveImages(outputDir string) (err error) {
	imgRGBA, _ := buildImage(0xff)

	var fPngAlpha *os.File
	fPngAlpha, err = os.OpenFile(filepath.Join(outputDir, "f.png"), os.O_WRONLY|os.O_CREATE, 0600)
	if err != nil {
		return err
	}
	defer func() {
		err = fPngAlpha.Close()
	}()
	err = png.Encode(fPngAlpha, imgRGBA)
	if err != nil {
		return err
	}

	var fJpg *os.File
	fJpg, err = os.OpenFile(filepath.Join(outputDir, "f.jpg"), os.O_WRONLY|os.O_CREATE, 0600)
	if err != nil {
		return err
	}
	defer func() {
		err = fJpg.Close()
	}()
	err = jpeg.Encode(fJpg, imgRGBA, nil)
	if err != nil {
		return err
	}

	var fGif *os.File
	fGif, err = os.OpenFile(filepath.Join(outputDir, "f.gif"), os.O_WRONLY|os.O_CREATE, 0600)
	if err != nil {
		return err
	}
	defer func() {
		err = fGif.Close()
	}()

	err = gif.Encode(fGif, imgRGBA, nil)
	if err != nil {
		return err
	}

	return
}

func isExists(filename string) bool {
	_, err := os.Stat(filename)
	return err == nil
}

func main() {
	flag.Parse()

	if flagOutputDir == "" {
		flag.Usage()
		os.Exit(1)
	}

	if !isExists(flagOutputDir) {
		os.MkdirAll(flagOutputDir, 0755)
	}

	checkErr(saveImages(flagOutputDir))

}
